package lesson3;

public class Else_If {
    public static void main(String[] args) {

        int a = 5;
        int b = 5;

        if (a == b)
        {
            System.out.println("a и b равны!");
        } else if (a == 6)
        {
            System.out.println("a = 6");
        } else
        {
            System.out.println("a и b НЕ равны!");
        }
    }
}
