package lesson3;


public class For {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++)
        {
            System.out.println(i + "\n" + "--------");
        }

        for (int s = 10; s >-11; s--)
        {
            System.out.println(s + "\n" + "--------");
        }
    }
}
